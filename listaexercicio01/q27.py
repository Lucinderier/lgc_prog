# Some 2 e 2 e 2 e armazene o resultado em uma variável soma.
soma = 2 + 2 + 2

# Multiplique a variável soma por -6 e armazene o resultado em uma variável chamada produto.
produto = soma * -6

# Use notação de augmented assignment para dividir a variável produto por 10 e armazenar o resultado na mesma variável.
