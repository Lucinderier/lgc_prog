valor_bool = True
valor_str_bool = str(valor_bool)

valor_int = 3
valor_float = float(valor_int)

valor_float_str = 3.8
valor_str_float = str(valor_float_str)

valor_float_int = 0.5
valor_int_from_float = int(valor_float_int)

valor_str = "4"
valor_int_from_str = int(valor_str)

# Exibe os resultados
print(f"True para str: {valor_str_bool}")
print(f"3 para float: {valor_float}")
print(f"3.8 para str: {valor_str_float}")
print(f"0.5 para int: {valor_int_from_float}")
print(f"'4' para int: {valor_int_from_str}")